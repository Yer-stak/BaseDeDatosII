     public void eliminarCarreraPregunta(){
        int pregunta;
        pregunta = JOptionPane.showConfirmDialog(null, "¿Realmente desea eliminar la carrera de la Base De Datos?","Confirmar Borrado",JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if (pregunta == 0) {
            metodos_sql.eliminarProfesor(txtClaveBuscarEliminar.getText());
            txtClaveBuscarEliminar.setText("");
            limpiar();
            limpiarcaja();
            bloquear();
        }   
    }
     
