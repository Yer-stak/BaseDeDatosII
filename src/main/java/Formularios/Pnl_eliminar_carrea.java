    public void buscarEliminarProfesor(String clave){
        try {
            conexion = Conexion_BD.conectar();
            String sql_busqueda_eliminacion = "SELECT * FROM carrera WHERE clave = ?";
            sentencia_preparada = conexion.prepareStatement(sql_busqueda_eliminacion);
            sentencia_preparada.setString(3, clave);
            resultado = sentencia_preparada.executeQuery();
            if (resultado.next()) {
                lblMensajeEliminar.setText("¡ATENCION: Clave REGISTRADO!");
                lblNombreEliminar.setText(resultado.getString("nombre_carrera"));
                lblIdEliminar.setText(resultado.getString("id"));
                desbloquear();
                
                
            }else{
                lblMensajeEliminar.setText("Sin resultados en la busqueda filtrada");
                bloquear();
                limpiar();
            }
            conexion.close();
        } catch (SQLException e) {
            System.out.println("Erro: "  + e);
        }finally{
            try {
                conexion.close();
            } catch (SQLException ex) {
                Logger.getLogger(Pnl_eliminar_profesor.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println("Error: " + ex);
            }
        }
        limpiarcaja();
    }
    
